print("Calculo de peso por sexo")
Pergunta = input('Coloque o valor M = Masculino e F = Feminino: ')

if Pergunta == "M" or "m":
    print('É MACHO')
elif Pergunta == 'F' or "f":
    print ('FEMALE')
